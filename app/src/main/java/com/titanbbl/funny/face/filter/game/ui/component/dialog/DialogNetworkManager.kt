object DialogNetworkManager {

    val dialog: DialogLoading? = null

    fun initDialogLoading(context: Context) {
        if (dialog == null) {
            DialogLoading(context)
        }
    }

    fun showDialogLoading() {
        dialog?.show()
    }


    fun dismissDialogLoading() {
        if (dialog?.isShowing == true) {
            dialog.dismiss()
        }
    }

}